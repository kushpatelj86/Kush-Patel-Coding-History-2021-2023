public class PrintStatements 
{
    public static void main(String[] args)//always the first method that runs
    {
        System.out.println("Hello World");//always end with ;
        System.out.print("Hello World");//doesnt skip line
        System.out.print("Hello World");//doesnt skip line

        System.out.println();//skips a line blank
        System.out.println("Hello World");//always end with ;

    }
}
