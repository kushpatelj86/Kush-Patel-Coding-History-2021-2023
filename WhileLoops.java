public class WhileLoops
{
    public static void main(String[] args)
    {
        //while loops are for looping if you don't know the exact number/unknown number of times
        int i = 0;
        while(i < 10)
        {
            System.out.println(i); //prints from 0-9
            i++; //we have to increment inside the loop or else we would get a infinite loop
        }
    }
}